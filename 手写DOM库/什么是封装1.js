// 什么叫封装
// 举例
// 笔记本电脑就是CPU 内存 硬盘 主板 显卡的封装
// 用户只需要接触显示器 键盘 鼠标 触控板等设备
// 就可操作复杂的计算机

// 接口
// 被封装的东西需要暴露一些功能给外部
// 这些功能就是接口 如USB接口 HDMI接口
// 设备只要支持这些接口 即可与被封装的东西通讯
// 比如键盘 鼠标支持USB接口
// 显示器支持HDMI接口

// 术语
// 库
// 我们把提供给其他人用的工具代码叫做库
// 比如jQuery

// API 
// 库暴露出来的函数或属性叫做API(应用编程接口)

// 框架
// 当你的库变得很大 并且需要学习才能看懂
// 那么这个库就叫框架 比如Vue/React


